function Highlight(obj) {
  obj.style.border = "1px solid red";
  obj.style.color = "red";
}
function DeHighlight(obj) {
  obj.style.border = "1px solid black";
  obj.style.color = "black";
}

function ChangeGrey() {
	document.body.style.backgroundColor = "#909090";
}

